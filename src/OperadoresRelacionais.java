public class OperadoresRelacionais {
    public static void main(String[] args) {
        // < > <= >= == !=

         boolean dezMaiorQueVinte = 10 > 20;
         boolean dezMenorQueVinte = 10 < 20;
         boolean dezMenorOuIgualVinte = 10 <= 20;
         boolean dezMaiorOuIgualVinte = 10 >= 20;
         boolean dezIgualVinte = 10 == 20;
         boolean dezDiferenteDeVinte = 10 != 20;

         System.out.println("dezMaiorQueVinte: " + dezMaiorQueVinte);
         System.out.println("dezMenorQueVinte: " + dezMenorQueVinte);
         System.out.println("dezMenorOuIgualVinte: " + dezMenorOuIgualVinte);
         System.out.println("dezMaiorOuIgualVinte: " + dezMaiorOuIgualVinte);
         System.out.println("dezIgualVinte: " + dezIgualVinte);
         System.out.println("dezDiferenteDeVinte: " + dezDiferenteDeVinte);


    }
}
